<h1>Règle des types de dégâts, les résistances et immunités</h1>

<div class="bloc">
	<div class="row margin-bottom">
		<p>
			Lors des combats et des péripéties, il peut arriver que les personnages se voit infligés des dégâts ou infligent eux-même des dégâts à d'autres personnages. <br/>
			Les dégâts sont classifiés par différents types qui influent sur la règle de calcul selon les résistances obtenues via les compétences et l'équipement de la créature qui les reçois.<br/>
			Les différents types de dégâts sont les suivants:
			<ul>
				<li><b>Asphyxie</b>: Représente la douleur épprouvée par une créature lorsqu'elle manque d'oxygène et suffoque.</li>
				<li><b>Brûlûre</b>: Une chaleur trop intense, une brûlûre, de la lave, du feu infligent des dégâts de brûlûre.</li>
				<li><b>Corrosion</b>: L'acide inflige des dégâts de corrosion.</li>
				<li><b>Force</b>: Un choc puissant, par exemple, un uppercut bien placé, un coup de marteau, ou une mauvaise chute infligent des dégâts de force.</li>
				<li><b>Foudroiement</b>: Un surplus d'électricité qui parcourt un corps vivant laisse en général des séquelles, c'est ce que représente ce type de dégâts.</li>
				<li><b>Froid</b>: Un froid mordant peut causer des effets douloureux.</li>
				<li><b>Nécrotique</b>: L'utilisation de la magie noire pour provoquer la décomposition d'une créature vivante.</li>
				<li><b>Perçant</b>: Un projectile ou une arme qui transperce un corps fait en général des dégâts de ce type.</li>
				<li><b>Psychique</b>: Lorsque c'est le mental qui prend les dégâts, par la peur, la folie ou d'autres afflictions. </li>
				<li><b>Radiant</b>: Une lumière sacrée puissante et aveuglante provoque des dégâts radiants.</li>	
				<li><b>Saignement</b>: La perte abondante de sang sappe rapidement la capacité à vivre de la plupart des créatures vivantes, c'est ce que représente ce type de dégâts.</li>
				<li><b>Sonore</b>: Un bruit insupportable peut causer ce type de dégâts.</li>
				<li><b>Tranchant</b>: Une coupure à l'épée, une lacération par les griffes d'un animal, tout ce qui provoque la déchirure de la chair entre dans cette catégorie de dégâts. </li>
			</ul>
		</p>
	</div>
	<div class="row margin-bottom">
		<p>
			Une créature ou un personnage peut avoir quatre statuts vis à vis d'un type de dégât: vulnérable, neutre, résistant ou immunisé. 
			Par exemple, un vampire est vulnérable aux dégâts radiants et résistant aux dégâts nécrotiques. En tant que mort-vivant, le vampire est immunisé aux dégâts de saignement et d'asphyxie.<br/>
			<ul>
				<li><b>Vulnérable</b>: Les dégâts sont doublés.</li>
				<li><b>Neutre</b>: Pas particulièrement de résistance ni de vulnérabilité, les dégâts restent tels qu'annoncés.</li>
				<li><b>Résistant</b>: Les dégâts sont réduis de moitié (arrondi à l'inférieur).</li>
				<li><b>Immunisé</b>: Les dégâts sont nuls.</li>
			</ul>
		</p>
	</div>
</div>