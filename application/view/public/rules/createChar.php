
<h1>Création de personnage</h1>

<div class="bloc">
	<div class="row margin-bottom">
		<p>Avant de commencer la partie, chaque joueurs choisit le personnage dont il va incarner le rôle tout au long d'une aventure, voire même des plusieurs aventures. Un personnage est défini par différents éléments: </p>
		<div class="align-center col-sm-2">
			<ul>
				<li><a href="caracteristiques">Des caractéristiques</a> qui offre une première idées des forces et faiblesses de votre personnage,</li>
				<li><a href="peuples">Un peuple ou race</a> auquel il appartient et dont il porte les caractéristiques,</li>
				<li><a href="niveau">Un niveau</a>, qui reflète l'expérience en tant qu'aventurier de votre personnage,</li>
				<li>Une identité composée de sa <a>personnalité</a>, son nom, son histoire et sa description physique,</li>
				<li><a href="competences">Un ensemble de compétences</a> et spécialisations dans certains domaines,</li>
				<li><a href="equipement"><a>De l'équipement</a> qu'il a accumulé au fil de son histoire.</a></li>
			</ul>
		</div>
	</div>
</div>