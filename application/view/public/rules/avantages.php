<h1>Règle des avantages et désavantages</h1>

<div class="bloc">
	<div class="row margin-bottom">
		<p>
			Lors de leurs aventures, les héros auront souvent a lancer des dés pour effectuer des tests demandés par le maître du jeu ou par l'utilisation de certaines compétences et autres. <br/>
			Il peut arriver que dans certains cas, les héros, ainsi que les créatures non joueurs disposent d'avantages ou de désavantages pour leur jets de 	dés, nous allons détailler ici tout ce qu'il y a a savoir sur cette mécanique de jeu.</p>
		<h2>Les avantages:</h2>
		<p>
			Un avantage représente la probabilité plus élevée qu'à un personnage ou une créature de réussir. Cet avantage se matérialise par le lancer d'un dé supplémentaire lors de son test, on conserve ainsi le dé dont la valeur obtenue est la plus élevée, puis on ajoute les éventuels bonus pour 	calculer le score final obtenu. 
		</p>
		<p>
			Les avantages sont cumulables, c'est-à-dire que si pour une action, on bénéficie de 2 avantages, on lancera donc trois dés au lieu d'un seul.
		</p>
		<p>
			Les avantages se gagnent via l'acquisition de <a href="competences">compétences</a> qui les octroient dans certaines circonstances, ou via l'utilisation d'un <a href="caracteristiques#PI">PI</a> par un compagnon. <br/>
			Un maître du jeu peut aussi décider d'accorder un avantage à un joueur s'il estime que celui-ci est mérité, ce peut être le cas lors d'un plan de jeu ingénieux mis en place par un joueur pour répondre à une problématique, par le respect du "role play" d'un personnage ou pour une interprétation de dialogue jugée convaincante entre autres exemples. <br/>
			A contrario, le maître du jeu peut aussi décider d'annuler un avantage s'il juge que ce dernier n'est pas justifié dans la situation rencontrée.
		</p>
			
	</div>
	<div class="row">
		<h2>Les désavantages</h2>
		<p>
			Un avantage représente la probabilité plus élevée qu'à un personnage ou une créature d'échouer. Cet avantage se matérialise par le lancer d'un dé supplémentaire lors de son test, on conserve ainsi le dé dont la valeur obtenue est la plus faible, puis on ajoute les éventuels bonus pour calculer le score final obtenu. 
		</p>
		<p>
			Les désavantages ne sont pas cumulables, on lancera donc toujours un maximum de 2 dés lorsqu'on est désavantagés. En revanche, un désavantage annule tout éventuel avantage. Si vous avez normalement un ou plusieurs avantages pour une action et que vous recevez un désavantage pour cette même action. Alors, vous ne lancerez qu'un seul dé.
		</p>
		<p>
			Les désavantages sont en général infligés par des <a href="competences">compétences</a> utilisées à votre encontre ou par des échec sur des tests. <br/>
			Un maître du jeu peut aussi décider de désavantager joueur sur une ou plusieurs action s'il estime que la situation le justifie, ce peut être par exemple le cas si un joueur est blessé et qu'il doit réaliser un test de force. <br/>
		</p>
	</div>
</div>